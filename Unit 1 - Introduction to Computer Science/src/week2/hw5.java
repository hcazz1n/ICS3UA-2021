package week2;
/**
 * Write program that can calculate the area of circle. 
 * You will need to create variables for the radius and the area.
 */
public class hw5 {
    public static void main(String[] args) {
        final double pi = 3.14159;
        int radius = 8;

        double area = pi * (radius * radius);

        System.out.println("The area is: " + area);


    }
}
